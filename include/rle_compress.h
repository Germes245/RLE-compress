#ifndef RLE_COMPRESS_H
#define RLE_COMPRESS_H

#include <stddef.h>   // для size_t

/**
 * @brief Выполняет RLE-сжатие (Run‑Length Encoding) массива байт.
 *
 * @param src                   указатель на исходные данные (интерпретируются как uint8_t)
 * @param length_of_src         количество байт во входном массиве
 * @param length_of_destination указатель на переменную, куда будет записана
 *                              длина сжатого массива в байтах
 *
 * @return указатель на выделенный буфер со сжатыми данными, или NULL при ошибке
 *         (некорректные параметры, недостаток памяти). Память выделяется через malloc(),
 *         вызывающий обязан освободить её через free().
 *
 * @note Формат сжатых данных: последовательность пар (счётчик, значение),
 *       где счётчик – число повторений (1…255), значение – сам байт.
 *       Серии длиннее 255 разбиваются на несколько блоков по 255.
 *       Алгоритм применяется всегда, даже для неповторяющихся данных,
 *       что может увеличить размер (каждый байт превращается в пару 1, байт).
 */
char* repeating_numbers_collect(char* src, size_t length_of_src, size_t* length_of_destination);

#endif // RLE_COMPRESS_H